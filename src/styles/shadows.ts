const materialUIBoxShadow = (styles: CSSProperties) => (value: number): CSSProperties => ({
  ...styles,
  ...materialUIBoxShadowElevations[value],
});

const ambientBoxShadow = (styles: CSSProperties) => (depth: number): CSSProperties => ({
  ...styles,
  boxShadow: `0 0 ${depth}px ${Math.floor(depth / 2)}px #00000040`,
});

export const shadows = chain((_styles, func) => ({
  box: {
    materialUI: {
      depth: func(materialUIBoxShadow),
    },
    ambient: func(ambientBoxShadow),
  },
}));
